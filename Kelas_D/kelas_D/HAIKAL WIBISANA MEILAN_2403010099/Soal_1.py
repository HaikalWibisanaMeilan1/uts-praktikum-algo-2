# Meminta pengguna untuk memasukkan jari-jari lingkaran
jari_jari = float(input("Masukkan jari-jari lingkaran (dalam satuan yang diinginkan): "))

# Menggunakan nilai Ï€
pi = 3.14

# Menghitung luas lingkaran
luas_lingkaran = pi * (jari_jari ** 2)

